	// 해당 패키지 번호의 이미지 가져오기
	public List<FileTravalPack> getFiles(Long festivalNum) {
		return fileTravalPackRepo.findByPackNumOrderByFileNum(festivalNum);
	}

	// 해당 패키지 번호의 이미지 이름만 가져오기
	public List<String> getName(Long festivalNum) {
		return fileTravalPackRepo.findFileNameByPackNumOrderByFileNum(festivalNum);
	}
	
	// 패키지 이미지 정보 저장
	public void submitFile(FileTravalPack fileTravalPack) {
		fileTravalPackRepo.save(fileTravalPack);
	}
	
	// 특정 이미지만 삭제
	@Transactional
	public void deleteFile(String fileName) {fileTravalPackRepo.deleteByFileName(fileName);}
	
	// 패키지 번호의 이미지 삭제
	@Transactional
	public void deleteAllFile(Long festivalNum) {fileTravalPackRepo.deleteAllByPackNum(festivalNum);}

