// ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒
	
	// 토큰 생성
	public String getToken(User user) {
    	
		// 기존에 저장된 토큰 정보 가져오기
        Optional<Token> _token = tokenRepository.findByUser_MemId(user.getMemId());

        // 아이디 권한 등을 토큰에 저장할 맵
        Map<String, Object> claims = new HashMap<>();
        claims.put("memId", user.getMemId());
        claims.put("role", user.getRole());
        
        // 랜덤한 전용 식별 코드 생성
        // https://adjh54.tistory.com/142
        String jti = UUID.randomUUID().toString();
        claims.put("jti", jti);
        
        // 토큰 생성
		String token = Jwts.builder()
            .setClaims(claims) // 토큰에 추가할 데이터
            .setSubject(user.getMemId()) // 토큰의 주제
			.setExpiration(new Date(System.currentTimeMillis() + EXPRIATIONTIME)) // 만료 기간
			.signWith(key) // 암호화 할 서명 키
			.compact(); // 토큰을 압축하여 문자열로 반환
        
        // 이미 존재하는 경우
        if (_token.isPresent()) { updateToken(_token.get(), jti, user); }
        
        // 새로 만드는 경우
        else { saveToken(jti, user); }
        
		return token;
	}

