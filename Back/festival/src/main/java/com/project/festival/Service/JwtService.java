// ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒
// ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒ ▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒

	// 토큰 생성
    private void saveToken(String jti, User user) {
        Token token = new Token();
        token.setJti(jti);
        token.setUser(user);
        token.setRole(user.getRole());
        token.setExpirationDate(new Date(System.currentTimeMillis() + EXPRIATIONTIME));
        tokenRepository.save(token);
    }

	// 토큰 업데이트(기본키인 토큰 번호가 null이 아닌 경우 수정(Update)가 된다)
    private void updateToken(Token token, String jti, User user) {
    	token.setJti(jti);
    	token.setUser(user);
    	token.setRole(user.getRole());
    	token.setExpirationDate(new Date(System.currentTimeMillis() + EXPRIATIONTIME));
        tokenRepository.save(token);
    }

